public class DataTypeArithmatics {

	public static void main(String[] args){
		float x = 10.0f;
		int y = 3;
		
		System.out.println("z = (x + y) : " + (x + y));
		System.out.println("z = (y + x) : " + (y + x));
		
		System.out.println("z = (x / y) : " + (x / y));
		System.out.println("z = (y / x) : " + (y / x));
	}

}